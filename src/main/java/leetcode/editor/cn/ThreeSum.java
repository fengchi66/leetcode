class Solution {
    public List<List<Integer>> threeSum(int[] nums) {

        int n = nums.length;
        ArrayList<List<Integer>> list = new ArrayList<List<Integer>>();

        for (int i = 0; i < n -2; i ++)
            for( int j = i + 1; j < n - 1; j++ )
                for( int k = j + 1; k < n; k++ )
                    if (nums[i] + nums[j] + nums[k] == 0)
                        list.add(Arrays.asList(nums[i], nums[j], nums[k]));

        return list;

    }
}
